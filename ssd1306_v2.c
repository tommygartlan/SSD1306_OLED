void SSD1306_SetCursor(uint8_t col, uint8_t row) 
{ 
    // Max 21 characters across (0-20), 8 rows down (0-7)
    if (col < 21 && row < 8)
    {
        cursorX = col * 6; 
        cursorY = row * 8; // This matches your ROW0, ROW1, etc. logic
    }
    else
    {
        cursorX = 0;  //Home position
        cursorY = 0;
    }
}

void SSD1306_WriteChar(char c, FontDef font, uint8_t color) 
{
    // Check if character is within the font range (ASCII 32 ' ' to 122 'z')
    if (c < 32 || c > 122) return;

    // Check if we have room on the current line
    if (SSD1306_WIDTH <= (cursorX + font.width)) {
        cursorX = 0;
        cursorY += font.height;
    }
    
    // Safety check for screen height
    if (SSD1306_HEIGHT < (cursorY + font.height)) {       //Modified <= to < to allow for ROW 8
        cursorX = 0;
        cursorY = 0;
    }

    // Copy the character font data into the display buffer
    for (uint8_t i = 0; i < font.width; i++) 
    {
        // Calculate index in the font array: (ASCII value - 32) * width + column
        uint8_t b = font.data[(c - 32) * font.width + i];
        
        // Calculate buffer index
        if (color == 1) {
            displayBuf[(cursorY / 8) * SSD1306_WIDTH + cursorX + i] = b;
        } else {
            displayBuf[(cursorY / 8) * SSD1306_WIDTH + cursorX + i] = 0;
        }
    }

    // Advance the cursor
    cursorX += font.width;
}

void SSD1306_WriteString(const char *str, FontDef font, uint8_t color) {
    while (*str) { 
        SSD1306_WriteChar(*str, font, color); 
        str++; 
    }
}

void SSD1306_PowerDown(void) {
    // 0xAE = Set Display OFF
    // This turns off the charge pump and the OLED panel
    ssd1306_WriteCmd(0xAE); 
}

void SSD1306_PowerUp(void) {
    // 0xAF = Set Display ON
    // This re-enables the charge pump and restores the previous image
    ssd1306_WriteCmd(0xAF); 
}